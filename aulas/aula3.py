"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Marçal Santarém')
print('1, Marçal "Santarém"')

# Aspas duplas
print("Marçal Santarém")
print(2, "Marçal 'Santarém'")

# Escape
print("Marçal \"Santarém\"")

# r
print(r"Marçal \"Santarém\"")
